--type : fac :: (Eq t, Num t) => t -> t
fac 0 = 1
fac n = n * fac(n-1)

-- implementation จากที่โจทย์ให้มานั้นปลอดภัย เนื่องจากมีการกำหนด base case เมื่อ n = 0 ไว้แล้ว โดยให้มีค่าเท่ากับ 1 เพื่อป้องกันการเกิด stack overflow 
-- และไม่มีผลต่อผลคูณที่เกิดขึ้นจาก function fac ที่เขียนไว้ เนื่องจากเมื่อสิ้นสุดกระบวนการก็จะเป็นการคูณ 1 กลับเข้าไปยังอนุกรมของผลคูณ